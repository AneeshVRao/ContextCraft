"""Evaluation harness for ContextCraft retrieval and generation.

Runs test queries against the currently indexed codebase and measures:
- Source hit rate (did the right file get retrieved?)
- Faithfulness (does the LLM answer match the ground truth context?)
- Latency (p50, p95 per query)
"""

import asyncio
import json
import logging
import statistics
import time
from pathlib import Path

from rich.console import Console
from rich.table import Table

from contextcraft.config import settings
from contextcraft.db import chunks_repo
from contextcraft.db.connection import close_pool, run_migrations
from contextcraft.embeddings.openai import OpenAIEmbedder
from contextcraft.llm.openai import OpenAILLM
from contextcraft.search.hybrid import hybrid_search

logging.basicConfig(level=logging.WARNING)
logger = logging.getLogger(__name__)
console = Console()

TEST_CASES_PATH = Path(__file__).parent / "test_cases.json"


async def llm_judge_faithfulness(question: str, ground_truth: str, generated_answer: str) -> bool:
    """Use an LLM to evaluate if the generated answer covers the ground truth."""
    llm = OpenAILLM()
    system_prompt = (
        "You are an evaluator for a RAG system. "
        "Your task is to determine if the GENERATED ANSWER successfully provides "
        "the information contained in the GROUND TRUTH for the given QUESTION. "
        "Respond ONLY with 'PASS' if it contains the information, or 'FAIL' if it misses it or hallucinates."
    )
    user_prompt = f"QUESTION: {question}\n\nGROUND TRUTH: {ground_truth}\n\nGENERATED ANSWER: {generated_answer}"

    response = await llm.generate(system_prompt, user_prompt)
    return "PASS" in response.upper()


async def warmup_system(repo_id: int):
    """Warm up the connection pool, embedding model, and LLM to prevent cold-start latency."""
    console.print("[dim]Warming up system...[/dim]")
    embedder = OpenAIEmbedder()
    llm = OpenAILLM()

    # Throwaway embedding & hybrid search
    query_emb = await embedder.embed_single("warmup")
    await hybrid_search(query_emb, "warmup", repo_id, top_k=2)

    # Throwaway LLM generation
    await llm.generate("system", "user")
    console.print("[dim]Warm-up complete.[/dim]\n")


async def run_evaluation(use_reranker: bool = False, use_deps: bool = False, runs: int = 3):
    """Run the evaluation suite multiple times to gather stable latency metrics."""
    test_cases = json.loads(TEST_CASES_PATH.read_text())

    await run_migrations()
    repos = await chunks_repo.list_repositories()
    if not repos:
        console.print("[red]No repos indexed. Please index the codebase first.[/red]")
        await close_pool()
        return

    repo_id = repos[0].id
    embedder = OpenAIEmbedder()
    llm = OpenAILLM()

    # Verify expected_sources are independent of the system's current performance
    console.print(
        "[yellow]Note: Ensure 'expected_sources' in test_cases.json were hand-verified, not generated by the system![/yellow]"
    )

    await warmup_system(repo_id)

    all_latencies = []
    all_hit_rates = []
    all_faithfulness = []

    config_name = "RRF Only"
    if use_reranker and use_deps:
        config_name = "RRF + Reranker + Deps"
    elif use_reranker:
        config_name = "RRF + Reranker"

    console.print(f"[bold]Running Evaluation ({config_name}) - {runs} Iterations[/bold]\n")

    for run_idx in range(1, runs + 1):
        console.print(f"[bold blue]=== Iteration {run_idx}/{runs} ===[/bold blue]")

        run_latencies = []
        run_hit_rates = []
        run_faithfulness = []

        for idx, tc in enumerate(test_cases, 1):
            question = tc["question"]
            expected_sources = tc["expected_sources"]
            ground_truth = tc["ground_truth"]

            # 1. Retrieval
            start_time = time.time()

            query_embedding = await embedder.embed_single(question)

            fetch_k = 20 if use_reranker else settings.search_top_k
            chunks = await hybrid_search(
                query_embedding=query_embedding,
                query_text=question,
                repo_id=repo_id,
                top_k=fetch_k,
            )

            if use_reranker:
                from contextcraft.reranker.cohere import CohereReranker

                reranker = CohereReranker()
                chunks = await reranker.rerank(question, chunks, settings.search_top_k)

            if use_deps:
                from contextcraft.graph.expander import expand_with_deps
                from contextcraft.models import SearchResult

                dep_chunks = await expand_with_deps([c.chunk.id for c in chunks], repo_id)
                # Append deps with dummy scores
                chunks.extend([SearchResult(chunk=dc, score=0.0, rank=100) for dc in dep_chunks])

            retrieval_time = time.time() - start_time
            run_latencies.append(retrieval_time)

            # Check source hit rate
            retrieved_files = {c.chunk.file_path for c in chunks}
            source_hits = [src for src in expected_sources if src in retrieved_files]
            hit_rate = len(source_hits) / len(expected_sources) if expected_sources else 0
            run_hit_rates.append(hit_rate)

            # 2. Generation
            context_text = "\n".join(
                [f"File: {c.chunk.file_path}\n{c.chunk.content}" for c in chunks]
            )
            system = "Answer the question based ONLY on the provided context."
            user = f"Context:\n{context_text}\n\nQuestion: {question}"

            answer = await llm.generate(system, user)

            # 3. Judge Faithfulness
            is_faithful = await llm_judge_faithfulness(question, ground_truth, answer)
            run_faithfulness.append(1 if is_faithful else 0)

            color = "green" if is_faithful and hit_rate > 0 else "red"
            console.print(
                f"[{idx}/{len(test_cases)}] [{color}]Hit Rate: {hit_rate:.1f} | Faithful: {is_faithful} | {retrieval_time:.2f}s[/{color}]"
            )

        all_latencies.extend(run_latencies)
        all_hit_rates.extend(run_hit_rates)
        all_faithfulness.extend(run_faithfulness)
        console.print()

    # Aggregate over all runs
    avg_hit_rate = statistics.mean(all_hit_rates)
    pct_faithful = (sum(all_faithfulness) / len(all_faithfulness)) * 100

    try:
        p50 = statistics.quantiles(all_latencies, n=2)[0]
        p95 = statistics.quantiles(all_latencies, n=20)[18]
    except statistics.StatisticsError:
        # Fallback if too few data points
        all_latencies.sort()
        p50 = all_latencies[len(all_latencies) // 2]
        p95 = all_latencies[int(len(all_latencies) * 0.95)]

    # Print summary
    table = Table(title=f"Evaluation Summary ({config_name}) over {runs} runs")
    table.add_column("Metric", style="cyan")
    table.add_column("Value", justify="right")

    table.add_row("Avg Source Hit Rate", f"{avg_hit_rate * 100:.1f}%")
    table.add_row("Faithful Answers", f"{pct_faithful:.1f}%")
    table.add_row("Retrieval p50 Latency", f"{p50:.3f}s")
    table.add_row("Retrieval p95 Latency", f"{p95:.3f}s")

    console.print(table)
    await close_pool()


if __name__ == "__main__":
    import sys

    use_rerank = "--rerank" in sys.argv
    use_deps = "--deps" in sys.argv
    runs_arg = 3
    if "--runs" in sys.argv:
        try:
            runs_idx = sys.argv.index("--runs")
            runs_arg = int(sys.argv[runs_idx + 1])
        except (ValueError, IndexError):
            pass

    asyncio.run(run_evaluation(use_reranker=use_rerank, use_deps=use_deps, runs=runs_arg))
